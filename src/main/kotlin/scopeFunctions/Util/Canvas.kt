package scopeFunctions.Util

class Canvas {
    fun rect(x: Int, y: Int, width: Int, height: Int) = println("$x, $y, $width, $height")
    fun circ(x: Int, y: Int, width: Int, height: Int) = println("$x, $y, $width, $height")
    fun text(x: Int, y: Int, string: String) = println("$x, $y, $string")
}